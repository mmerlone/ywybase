/**
 * @fileoverview Script to backup complete Supabase database schema and data.
 *
 * This script connects to your Supabase project and exports:
 * - Complete database schema (tables, indexes, constraints)
 * - All database functions and triggers
 * - Row Level Security (RLS) policies
 * - All table data as SQL INSERT statements
 * - Extension configurations
 * - Scheduled jobs (pg_cron)
 *
 * The backup is saved as a timestamped SQL file that can be used for:
 * - Database restoration
 * - Migration to another project
 * - Schema versioning
 * - Disaster recovery
 *
 * @example
 * ```bash
 * # Run the script
 * pnpm run backup:db
 *
 * # Or run directly with ts-node
 * npx ts-node scripts/generate-supabase-backup.ts
 * ```
 *
 * @requires NEXT_PUBLIC_SUPABASE_PROJECT_ID or SUPABASE_PROJECT_ID in .env.local
 * @requires Supabase CLI to be installed globally
 * @requires psql (PostgreSQL client) to be installed
 *
 * @author YwyBase Team
 * @since 1.0.0
 */

import { execSync } from 'child_process'
import { writeFileSync, mkdirSync, existsSync } from 'fs'
import { join } from 'path'
import dotenv from 'dotenv'

// Load environment variables from .env.local
dotenv.config({ path: join(process.cwd(), '.env.local') })

// Configuration
const SUPABASE_PROJECT_ID = process.env.NEXT_PUBLIC_SUPABASE_PROJECT_ID ?? process.env.SUPABASE_PROJECT_ID
const BACKUP_DIR = join(process.cwd(), 'backups')
const TIMESTAMP = new Date().toISOString().replace(/[:.]/g, '-').slice(0, -5)
const BACKUP_FILE = join(BACKUP_DIR, `backup-${TIMESTAMP}.sql`)

if (!SUPABASE_PROJECT_ID) {
  console.error('❌ Error: SUPABASE_PROJECT_ID is not defined in .env.local')
  process.exit(1)
}

